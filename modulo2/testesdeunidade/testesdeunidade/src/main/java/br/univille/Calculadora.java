package br.univille;

public class Calculadora {
    public int soma(int num1, int num2) {
        return num1 + num2;
    }

    public int sub(int num1, int num2) {
        return num1 - num2;
    }

    public float div(float num1, float num2) {
        return num1 / num2;
    }

    public float div(int num1, int num2) {
        return num1 / num2;
    }
}
